package Data;

public class Temp {

 @Override
	public String toString() {
		return "Temp [temp=" + temp + ", date=" + date + "]";
	}
private String temp;
 private String date;
public String getTemp() {
	return temp;
}
public void setTemp(String temp) {
	this.temp = temp;
}
public String getDate() {
	return date;
}
public void setDate(String date) {
	this.date = date;
}
 
 
}
